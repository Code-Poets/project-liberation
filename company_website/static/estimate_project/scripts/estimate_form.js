$( ".add-button" ).each(function(){
    $(this).click(function() {
        $('.bottom-containers').css('display', 'block');
        $('.hide-add-questions').css('display', 'block');
        $('.additional-questions').css('display', 'none');
        $('.submit-container').css('display', 'none');
    });
  });

$( "#hide_questions" ).click(function() {
    $('.bottom-containers').css('display', 'none');
    $('.hide-add-questions').css('display', 'none');
    $('.additional-questions').css('display', 'block');
    $('.submit-container').css('display', 'block');
  });
